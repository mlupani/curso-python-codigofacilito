my_tuple = (1, 2, 3, 4, 5)

#len
print(len(my_tuple))

#sorted
print(sorted(my_tuple))

#sorted reverse
print(sorted(my_tuple, reverse=True))

#count (cuantas veces aparece un elemento en la tupla)
print(my_tuple.count(1))

#in (verificar si un elemento existe en la tupla)
print(1 in my_tuple)

#index (retorna el indice de un elemento en la tupla)
print(my_tuple.index(1))

#max (retorna el valor maximo de la tupla)
print(max(my_tuple))

#min (retorna el valor minimo de la tupla)
print(min(my_tuple))



