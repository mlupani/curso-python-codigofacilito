my_list = ["Juan", "Perez", 20]

longitud = len(my_list)

print("la longitud de la lista es: ", longitud)

#podemos obtener los indices con numeros negativos

print("el ultimo elemento de la lista es: ", my_list[-1])

#podemos obtener un rango de elementos

print("los dos primeros elementos de la lista son: ", my_list[0:2])

# podemos modificar los elementos de la lista
my_list[0] = "Nuevo nombre"

print("el primer elemento de la lista es: ", my_list[0])


